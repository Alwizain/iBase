    private void tabelkeluarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelkeluarMouseClicked
        int baris = tabelkeluar.rowAtPoint(evt.getPoint());
        String id = tabelkeluar.getValueAt(baris, 1).toString();
        txtidbarang.setText(id);
        
        String nama = tabelkeluar.getValueAt(baris, 2).toString();
        txtnama.setText(nama);
        
        String jenis = tabelkeluar.getValueAt(baris, 3).toString();
        cbjenis.setSelectedItem(jenis);
        
        String berat = tabelkeluar.getValueAt(baris, 4).toString();
        txtberat.setText(berat);
        
        String tglm = tabelkeluar.getValueAt(baris, 5).toString();
        txttglkeluar.setText(tglm);
    }//GEN-LAST:event_tabelkeluarMouseClicked

